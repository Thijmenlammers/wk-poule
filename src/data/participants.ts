import { poolMatches } from "@/data/pool-matches";
import type { MatchPrediction, Participant } from "@/types";

type Score = readonly [home: number, away: number];

interface ParticipantScorecard {
  id: string;
  name: string;
  predictedChampion: string;
  scores: readonly Score[];
}

function createPredictions(scores: readonly Score[]): MatchPrediction[] {
  if (scores.length !== poolMatches.length) {
    throw new Error(
      `A participant scorecard must contain ${poolMatches.length} predictions.`,
    );
  }

  return poolMatches.map((match, index) => ({
    matchId: match.id,
    predictedHomeScore: scores[index][0],
    predictedAwayScore: scores[index][1],
  }));
}

